    public void readJTable(){
        
          DefaultTableModel modelo=(DefaultTableModel) jTable1.getModel();
          ArrayList<Token> tokens = new ArrayList<Token>();
          Token token = null;
          Token token2 = null;
          try {
		Scanner sc = new Scanner("input.isi");
		IsiParser  pa = new IsiParser(sc);
			
			//pa.E();
			
		System.out.println("Compilation Successful!");
                
        
        do{
           
            token = sc.nextToken();
             
          
            if(token != null){
             System.out.println(token);
              tokens.add(token);  
                
            }
        } while(token != null);
		} catch (IsiLexicalException ex) {
			System.out.println("Lexical Error "+ex.getMessage());
                        int linha;
                        if(token==null){
                           jTextArea2.setText("Erro Lexico: "+ex.getMessage()+" na linha "+1); 
                        }
                        else{
                        linha = token.getLine()+1;
                        jTextArea2.setText("Erro Lexico: "+ex.getMessage()+" na linha "+linha);
                        }
		}
		catch (IsiSyntaxException ex) {
			System.out.println("Syntax Error "+ex.getMessage());
		}
		catch (Exception ex) {
			System.out.println("Generic Error!!");
			System.out.println(ex.getClass().getName());
		}
          
          modelo.setNumRows(0);
                tokens.forEach((t) -> {
                    modelo.addRow(new Object[]{
                        t.getTipo(),
                        t.getText(),
                        t.getLine(),
                        t.getColumn()
                });});
               //jTextArea2.setText("Resultados da complilacao:...\n"+token.getErro());
    }

